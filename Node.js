function sumar(x,y) {
    let resultado = x + y
    return resultado
}
function restar(x,y) {
    let resultado = x - y
    return resultado
}
function producto(x,y) {
    let resultado = x * y
    return resultado
}
function dividir(x,y) {
    let resultado = x / y
    return resultado
}
function cuadrado(x){
    let resultado = Math.sqrt(x);
    return resultado
}